const int MX = (int)1e4 + 5;

void solve([[maybe_unused]] int test) {
    int n;
    scanf("%lld", &n);
    vector<int> a(n);
    for (int i = 0; i < n; i++) {
        scanf("%lld", &a[i]);
    }
    sort(a.begin(), a.end());
    bool ans = true;
    int sum = 1;
    if (a[0] != 1) ans = false;
    for (int i = 1; i < n; i++) {
        if (a[i] > sum) ans = false;
        sum += a[i];
    }
    ans ? printf("YES\n") : printf("NO\n");
}

int32_t main() {
    int t = 1;
    cin >> t;
    for (int tt = 1; tt <= t; tt++) {
        solve(tt);
    }
}
