void solve([[maybe_unused]] int test) {
    string s;
    cin >> s;
    set<int> a;
    for (auto c : s) {
        a.insert(c - '0');
    }
    int ans = 0;
    while (!a.count(ans)) {
        ans++;
    }
    printf("%lld\n", ans);
}

